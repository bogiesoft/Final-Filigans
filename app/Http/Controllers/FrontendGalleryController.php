       if($request->ajax())
       {
        $skip = $request->get("skip", 0);
        $take = $request->get("take", 10);

        $gallery = Gallery::skip($skip)->take($take)->get();
        return $gallery;
    }
    $gallery = Gallery::take(10)->get();
    
    return view("frontend.gallery.index", compact("gallery"));
